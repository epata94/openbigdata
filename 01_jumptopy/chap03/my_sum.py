def my_sum(a,b):
    result = a+b
    print("Calculation Start!")
    print("First Parameter a: ",a)
    print("Second Parameter b: ",b)
    print("Internal Expression: a+b")
    print("Calculation Result: ",result)
    print("Calculation End")
    print("")

    return result

print("my_sum program is initiated..")
print("")
my_sum(1,2)
