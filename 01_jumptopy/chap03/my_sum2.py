    result = a+b
    print("연산 시작")
    print("첫 번째 인자 a: ",a)
    print("두 번째 인자 b: ",b)
    print("내부 연산식: a+b")
    print("연산 결과: ",result)
    print("연산 종료")
    print("")

    return result

